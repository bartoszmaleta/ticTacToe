function changeToCircle1() {
    let cells = document.querySelectorAll('.cell');
    cells.forEach(cell => {
        if (cell.classList.contains('circle')) {
            console.log(cell);
            cell.style = 'background-image: url(../resources/icon/circle/circle1.png)';        
        }
    });
}

