function changeToCross1() {
    let cells = document.querySelectorAll('.cell');
    cells.forEach(cell => {
        if (cell.classList.contains('x')) {
            console.log(cell);
            cell.style = 'background-image: url(../resources/icon/cross/cross1.svg)';
            // cell.style = 'background-position: center';
            // cell.style = 'background-repeat: no-repeat';
        }
    });
}

